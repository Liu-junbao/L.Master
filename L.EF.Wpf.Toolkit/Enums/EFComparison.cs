using System;
using System.Collections.Generic;
using System.Text;

namespace System
{
    /// <summary>
    /// 比较
    /// </summary>
    public enum EFComparison
    {
        /// <summary>
        /// 等于 
        /// ==
        /// </summary>
        Equal,
        /// <summary>
        /// 不等于 
        /// !=
        /// </summary>
        NotEqual,
        /// <summary>
        /// 大于
        /// >
        /// </summary>
        GreaterThan,
        /// <summary>
        /// 大于或等于
        /// >=
        /// </summary>
        GreaterThanOrEqual,
        /// <summary>
        /// 小于
        /// </summary>
        LessThan,
        /// <summary>
        /// 小于或等于
        /// </summary>
        LessThanOrEqual,
        /// <summary>
        /// 包含
        /// </summary>
        Contains,
        /// <summary>
        /// 不包含
        /// </summary>
        NotContains,
        /// <summary>
        /// 开头
        /// </summary>
        StartWith,
        /// <summary>
        /// 尾部
        /// </summary>
        EndWith,
    }


}
